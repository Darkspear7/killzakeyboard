function Rect( x, y, width, height){
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
}

function Point(x,y){
    this.x = x;
    this.y = y;
}

function Circle(x, y, radius){
    this.x = x;
    this.y = y;
    this.radius = radius;
}

function Size(width, height) {
    this.width = width;
    this.height = height;
}